//declarando variaveis
const raioBase = 3;
const alturaCilindro = 1;
const pi = 3.14;
const areaCilindro = 2 * pi * raioBase * (raioBase + alturaCilindro);
//imprimindo valor da área do cilindro
console.log(`A área do cilindro de raio = ${raioBase} e altura = ${alturaCilindro}, é ${areaCilindro}.`);

const diaDaSemana = 10;

if (diaDaSemana == 1) {
    console.log("É uma segunda-feira.");
} else if (diaDaSemana == 2) {
    console.log("É uma terça-feira.");
} else if (diaDaSemana == 3) {
    console.log("É uma quarta-feira.");
} else if (diaDaSemana == 4) {
    console.log("É uma quinta-feira.");
} else if (diaDaSemana == 5) {
    console.log("É uma sexta-feira.");
} else if (diaDaSemana == 6) {
    console.log("É um sábado");
} else if (diaDaSemana == 7) {
    console.log("É um domingo");
} else {
    console.log("O dia da semana informado não é válido");
}